class VigenereCipheringMachine {

  constructor(isDirect = true) {
    this.isReverse = isDirect ? false : true;
  }

  encrypt(message, key) {
    if (!message || !key) throw new Error('Incorrect arguments!');

    key = key.toUpperCase().split('');
    message = message.toUpperCase().trim().split('');



    let tmpIndex = 0;
    let result = [];

    for (let item of message) {
      if (alphabet[0].includes(item)) {
        let indexKey = alphabet[0].indexOf(key[tmpIndex]);
        let indexMessage = alphabet[0].indexOf(item)
        result.push(alphabet[indexMessage][indexKey])
        tmpIndex++;
        if (!key[tmpIndex]) tmpIndex = 0;
      } else result.push(item)
    }

    if (this.isReverse) return result.reverse().join('');
    return result.join('')
  }

  decrypt(message, key) {
    if (!message || !key) throw new Error('Incorrect arguments!');

    key = key.toUpperCase();
    message = message.toUpperCase().trim();

    let tmpIndex = 0;
    let result = [];

    for (let item of message) {
      if (alphabet[0].includes(item)) {
        let indexKey = alphabet[0].indexOf(key[tmpIndex]);
        tmpIndex++;

        if (!key[tmpIndex]) tmpIndex = 0;
        const module = alphabet[0].length;
        const indexMessage = alphabet[0].indexOf(item)
        const valueIndex = ((indexMessage - indexKey) % module > 0) ? (indexMessage - indexKey) : (indexMessage - indexKey + module) % module
        result.push(alphabet[0][valueIndex]);
      } else result.push(item)
    }

    if (this.isReverse) return result.reverse().join('');
    return result.join('')
  }
}

